const i18n = {
    currentLang: localStorage.getItem('lang') || 'en',

    init() {
        this.applyTranslations();
    },

    setLanguage(lang) {
        this.currentLang = lang;
        localStorage.setItem('lang', lang);
        this.applyTranslations();
    },

    translate(key) {
        return translations[this.currentLang][key] || key;
    },

    applyTranslations() {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            const translation = this.translate(key);
            
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translation;
            } else if (el.tagName === 'SELECT') {
                // If the first option is a placeholder
                if (el.options[0] && el.options[0].disabled) {
                    el.options[0].innerText = translation;
                }
            } else {
                el.innerText = translation;
            }
        });

        const langToggle = document.getElementById('langToggleText');
        if (langToggle) {
            langToggle.innerText = this.currentLang === 'en' ? 'हिन्दी' : 'English';
        }

        // Sync dropdowns
        ['signupLang', 'dashboardLang'].forEach(id => {
            const el = document.getElementById(id);
            if (el) el.value = this.currentLang;
        });
    }
};

// Auto-init for pages that include this script
document.addEventListener('DOMContentLoaded', () => i18n.init());
